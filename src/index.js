const MORSE_TABLE = {
  '.-':     'a',
  '-...':   'b',
  '-.-.':   'c',
  '-..':    'd',
  '.':      'e',
  '..-.':   'f',
  '--.':    'g',
  '....':   'h',
  '..':     'i',
  '.---':   'j',
  '-.-':    'k',
  '.-..':   'l',
  '--':     'm',
  '-.':     'n',
  '---':    'o',
  '.--.':   'p',
  '--.-':   'q',
  '.-.':    'r',
  '...':    's',
  '-':      't',
  '..-':    'u',
  '...-':   'v',
  '.--':    'w',
  '-..-':   'x',
  '-.--':   'y',
  '--..':   'z',
  '.----':  '1',
  '..---':  '2',
  '...--':  '3',
  '....-':  '4',
  '.....':  '5',
  '-....':  '6',
  '--...':  '7',
  '---..':  '8',
  '----.':  '9',
  '-----':  '0'
};

function decode(expression) {
  let result = expression.split('**********').map(
    a => a.match(/.{10}/g).map(
      b => b.match(/.{2}/g).map(
        letter => letter.replace('00', '').replace('10', '.').replace('11','-')
      ).join('')
    ).map(
      encoded => MORSE_TABLE[encoded]
    ).join('')
  ).join(' ');

  return result;
}

module.exports = {
  decode
}